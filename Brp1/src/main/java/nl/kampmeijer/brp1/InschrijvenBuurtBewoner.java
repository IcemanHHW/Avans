package nl.kampmeijer.brp1;

public class InschrijvenBuurtBewoner {
    private String soortnaam;
    private String variantnaam;
    private String datum;
    private String starttijd;
    private String locatienaam;
    private String buurtbewonernaam;

    public InschrijvenBuurtBewoner() {

    }

    public String getSoortnaam() {
        return soortnaam;
    }

    public void setSoortnaam(String soortnaam) {
        this.soortnaam = soortnaam;
    }

    public String getVariantnaam() {
        return variantnaam;
    }

    public void setVariantnaam(String variantnaam) {
        this.variantnaam = variantnaam;
    }

    public String getDatum() {
        return datum;
    }

    public void setDatum(String datum) {
        this.datum = datum;
    }

    public String getStarttijd() {
        return starttijd;
    }

    public void setStarttijd(String starttijd) {
        this.starttijd = starttijd;
    }

    public String getLocatienaam() {
        return locatienaam;
    }

    public void setLocatienaam(String locatienaam) {
        this.locatienaam = locatienaam;
    }

    public String getBuurtbewonernaam() {
        return buurtbewonernaam;
    }

    public void setBuurtbewonernaam(String buurtbewonernaam) {
        this.buurtbewonernaam = buurtbewonernaam;
    }
}
